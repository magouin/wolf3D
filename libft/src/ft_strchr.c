#include "../includes/libft.h"

char	*ft_strchr(const char *s, int d)
{
	int c;

	c = 0;
	while (s[c] != d)
	{
		if (s[c] == '\0')
			return (NULL);
		c++;
	}
	return ((char*)(s + c));
}
